package cn.chinat2t.cloud.bean;

public class BrandPicsDetailBean {
	private String linkageid;
	private String name;
	private String logo;
	private String ppxx;
	private String ppjs;
	public String getLinkageid() {
		return linkageid;
	}
	public void setLinkageid(String linkageid) {
		this.linkageid = linkageid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	public String getPpxx() {
		return ppxx;
	}
	public void setPpxx(String ppxx) {
		this.ppxx = ppxx;
	}
	public String getPpjs() {
		return ppjs;
	}
	public void setPpjs(String ppjs) {
		this.ppjs = ppjs;
	}
	
}
